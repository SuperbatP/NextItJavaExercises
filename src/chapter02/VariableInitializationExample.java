package chapter02;
//변수는 선언 및 초기화가 반드시 필수
public class VariableInitializationExample {
    public static void main(String[] args) {
        //선언
        int age;
        //초기화
       age = 10;
        System.out.println(age);
        int value=0;
        int result=value+10;

        System.out.println(result);
    }
}